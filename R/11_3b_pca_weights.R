# =============================================================================
cat(paste(rep("=", 70), collapse = ""), "\n")
cat("PCA ANALYSIS COMPLETE\n")
cat(paste(rep("=", 70), collapse = ""), "\n\n")
cat("  Retained PCs       : ", paste("PC", retained_pcs, collapse = ", "), "\n", sep = "")
cat("  Selected indicators: ", paste(unique_indicators, collapse = ", "), "\n", sep = "")
cat("  Weights:\n")
for (i in seq_len(nrow(weight_df))) {
  cat("    ", weight_df$Indicator[i], " = ", weight_df$Weight[i],
      " (from ", weight_df$PC[i], ")\n", sep = "")
}
