cat("    ", contrib_png, "\n", sep = "")
cat("    ", biplot_png, "\n", sep = "")
cat("    ", weights_png, "\n", sep = "")

cat("\nNext: source('R/11_3b_biomass_production_utility.R') to use these weights\n")
cat(paste(rep("=", 70), collapse = ""), "\n")
